            <h6>16. ¿Cuál es la diferencia entre un cronógrafo y un cronómetro?</h6>
            <p style="text-align: justify;">Un cronógrafo es un reloj que, además de indicar mediante agujas las horas, los minutos y los segundos, incluye un mecanismo que mide y registra un tiempo parcial transcurrido, mediante un segundero central de cronógrafo y contadores, no siempre, de minutos y de horas.
            <br>Un cronómetro es un reloj cuyo movimiento ha obtenido un certificado oficial de precisión otorgado por el COSC (Control Oficial Suizo de Cronómetros), tras superar varias pruebas de precisión en diferentes posiciones y temperaturas. Estas pruebas se realizan durante un periodo de 15 días.</p>
            <h6>17. ¿Qué es un movimiento de cuarzo?</h6>
            <p style="text-align: justify;">Los relojes de cuarzo utilizan la oscilación que produce una pieza de cuarzo para definir el tiempo. Todos los relojes de Swatch tienen movimiento de cuarzo, excepto los Automatic, que incluyen todos los modelos Sistem51. El cuarzo tiene la propiedad de crear oscilaciones a una frecuencia precisa cuando es estimulado electrónicamente. Un oscilador de cristal consiste en un circuito electrónico que usa la resonancia mecánica de un cristal vibratorio de material piezoeléctrico para generar una señal eléctrica con una frecuencia muy precisa. Esta frecuencia se usa habitualmente para seguir el paso del tiempo (al igual que en los relojes de pulsera de cuarzo), con el fin de ofrecer una señal de reloj estable para circuitos digitales integrados y para estabilizar frecuencias para transmisores y receptores de radio. El tipo más habitual de resonadores piezoeléctricos utilizado es el cristal de cuarzo, de modo que los circuitos de osciladores diseñados con él han recibido el nombre de “osciladores de cristal”. En otras palabras: el cuarzo proporciona una vibración estable que emite impulsos regulares. Mediante un “circuito integrado”, los impulsos se envían a los engranajes de las agujas.<p>
            <h6>18. ¿Qué es a prueba de golpes?</h6>
            <p style="text-align: justify;">Los relojes que indican “resistente a los golpes” son relojes en los que el efecto residual en la precisión después de recibir golpes del nivel contemplado en las normas no supera los 2 segundos al día en el caso de relojes de cuarzo y 60 segundos al día para el resto, según la norma ISO 1413 (basada en la simulación del golpe que recibe un reloj si cae accidentalmente desde una altura de 1 m a una superficie horizontal de madera dura), o NIHS 91-10, la norma equivalente de la industria relojera suiza.</p>
            <h6>19. ¿Qué es un automático?</h6>
            <p style="text-align: justify;">Los modelos Automatic y Sistem51 de Swatch son relojes mecánicos que se dan cuerda automáticamente. La potencia que necesitan para seguir funcionando no proviene de una pila, sino de la energía cinética natural creada por los movimientos del brazo del usuario. Los relojes automáticos reaccionan de forma más sensible a las fluctuaciones de temperatura, a los golpes y al modo de llevarlos. Por ello, un reloj automático no funciona con la misma precisión que un reloj de cuarzo: presenta unas imprecisiones que van de -10 a +40 segundos por día. Por nuestra experiencia, sabemos que los relojes mecánicos tienden a la imprecisión cuando son nuevos. Esto suele regularse con el paso del tiempo.</p>
            <h6>20. ¿Qué es Autoquartz?</h6>
            <p style="text-align: justify;">El Autoquartz de Swatch consiste en una tecnología que combina las ventajas de un movimiento automático sin pilas con la precisión y la fiabilidad del cuarzo. La energía cinética generada por el peso oscilante del Autoquartz se transforma en electricidad. Esta se almacena en un condensador y se supervisa mediante un oscilador de cuarzo y un circuito eléctrico diminuto para lograr la clase de precisión que suele asociarse al cuarzo. El condensador conserva suficiente reserva de energía para mantener el reloj en marcha durante 100 días.</p>




